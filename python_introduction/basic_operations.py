# Declaring two numbers
number1 = 10
number2 = 5

# Addition
sum = number1 + number2
print("Addition of 10 and 5 is", sum)

# Subtraction
difference = number1 - number2
print("Subtraction of 10 and 5 is", difference)

# Multiplication
product = number1 * number2
print("Multiplication of 10 and 5 is", product)